<h3>QueryBuilder</h3>
QueryBuilder - это набор функций для быстрого и удобного доступа к базе данных и её содержимому. 
Данный QueryBuilder написан с использованием ООП без фреймворков. 
Реализованые следующие методы: 
INSERT, SELECT, UPDATE, DELETE. 
Реализованы условия: 
LIMIT, WHERE, ORDER. 
Все настройки сохранены в файле config.php из этого же каталога. 
